/*
陣列過濾

建立一個陣列 nums = [1, 2, 3, 4, 5, 6, 7, 8, 9]。
過濾出所有的偶數，並輸出新的陣列。
*/

let nums = [1, 2, 3, 4, 5, 6, 7, 8, 9];
let evenNumbers = nums.filter(num => num % 2 === 0);// 使用 filter() 過濾出偶數
console.log(evenNumbers);// 輸出: [2, 4, 6, 8]

/*

filter() 方法會遍歷 nums 陣列中的每個元素，並將每個元素傳遞給你提供的函式（在這裡是箭頭函式 num => num % 2 === 0）。
num % 2 === 0 是條件，它檢查每個數字是否能被 2 整除（即是否是偶數）。
如果條件為真，num 就會被加入到新陣列中。


=== 是 JavaScript 中的 嚴格相等運算符（strict equality operator）。它用來比較兩個值是否 相等，並且會同時檢查 值和型別 是否相同。
=== 會檢查 值 和 型別：
如果兩個值 相等，並且型別也 相同，則會返回 true。
如果值 不相等，或者型別 不同，則返回 false。


*/