/*
while 迴圈倒數計時

宣告變數 count = 5。
使用 while 迴圈讓 count 依序輸出 5, 4, 3, 2, 1, 0。
*/

let count = 5;
while(count >= 0){
    console.log(count);
    count--;

}


/*
解釋：
let count = 5; 宣告變數 count 並初始化為 5。
while (count >= 0) 這個條件表示當 count 大於或等於 0 時，迴圈會繼續執行。
console.log(count); 每次迴圈會印出當前的 count 值。
count--; 在每次迴圈結束時，count 減 1，這樣每次會依序輸出 5、4、3、2、1、0。
*/
